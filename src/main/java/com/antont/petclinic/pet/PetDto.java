package com.antont.petclinic.pet;

import javax.validation.constraints.NotNull;

public class PetDto {

    @NotNull
    private String name;

    private String type;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
